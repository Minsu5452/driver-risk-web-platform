// ── SHAP 집계 유틸 함수 ──

/**
 * 차트용: 카테고리 집계 (A1~B10 단위)
 * shapValues: [{feature: "A1-1", value: 0.03}, ...]
 * returns: [{category: "A1", label: "속도예측검사", value: 0.05}, ...]
 */
export function aggregateShapByCategory(shapValues) {
    const map = {};
    for (const { feature, value } of shapValues) {
        const m = feature.match(/^([AB]\d+)/);
        if (!m) continue;
        map[m[1]] = (map[m[1]] || 0) + value;
    }
    return Object.entries(map).map(([cat, val]) => ({
        category: cat,
        label: FEATURE_MAPPINGS[cat] || cat,
        value: val,
    }));
}
